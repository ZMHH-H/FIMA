def _get_clones(module, N):
    return nn.ModuleList([copy.deepcopy(module) for i in range(N)])


def _get_activation_fn(activation):
    if activation == "relu":
        return F.relu
    elif activation == "gelu":
        return F.gelu

    raise RuntimeError("activation should be relu/gelu, not {}".format(activation))


class PositionalEncoding(nn.Module):
    """ 
        1D sin_cos absolute Positional encoding.
        num_hiddens: d_model, feature dim
        max_len: the maximum possible seq_length of input sequence
    """
    def __init__(self, num_hiddens, max_len=1000):
        super().__init__()
        
        assert num_hiddens % 2 == 0, "the dimension of model must be even"

        # Create a long enough P
        self.P = torch.zeros((1, max_len, num_hiddens))

        # pos/[max_len**( 2*i / d_model)]
        # [max_len, num_hiddens/2]
        X = torch.arange(max_len, dtype=torch.float32).reshape(
            -1, 1) / torch.pow(10000, torch.arange(0, num_hiddens, 2, dtype=torch.float32) / num_hiddens)
        # [1, max_len, num_hiddens]
        self.P[:, :, 0::2] = torch.sin(X)
        self.P[:, :, 1::2] = torch.cos(X)

        # [max_len, 1 , num_hiddens]
        self.P = self.P.permute(1,0,2)  # [N,S,E]-->[S,N,E]
        
    ''' input : X: [S,N,E] '''
    def forward(self, X):
        X = X + self.P[:X.shape[0],:,:].to(X.device)
        return X
        #return self.dropout(X)

